# modules in python
# A module in Python is a file containing Python code—typically functions, classes, or variables—that you can import and reuse in other Python programs.

import math as m # Importing the built-in math module with an alias 'm'
#built-in modules are modules that are included with Python by default, so you don't need to install them separately.

print("Square root of 16:", m.sqrt(16))
print("Power 2^3:", m.pow(2, 3))
print("Factorial of 5:", m.factorial(5))
print("Cos(0):", m.cos(0))
print("Radians of 180 degrees:", m.radians(180))
print("π:", m.pi)



